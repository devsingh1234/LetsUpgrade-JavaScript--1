function turnOn() {
  ele = document.getElementsByClassName("image");
  ele[0].src = "pic_bulbon.gif";
}
function turnOff() {
  ele = document.getElementsByClassName("image");
  ele[0].src = "pic_bulboff.gif";
}

function turnRed() {
  ele = document.getElementsByClassName("image");
  ele[0].src = "pic_bulbred.gif";
}
